import streamlit as st
import os
import cv2
import matplotlib.pyplot as plt

from config import DATASET_PATH

def show_dataset():

    dataset_path = st.text_input(
        "Dataset Folder",
        os.path.join(DATASET_PATH, "train")
    )
    st.write("Dataset path:", dataset_path)
    st.subheader("📁 Dataset Information")

    if os.path.exists(dataset_path):

        for folder in os.listdir(dataset_path):
            path = os.path.join(dataset_path, folder)

            if os.path.isdir(path):
                st.write(f"**{folder}** : {len(os.listdir(path))} images")

    else:
        st.error("Dataset folder not found!")
        st.stop()

    st.subheader("📂 Jumlah Dataset Awal")

    classes = sorted(os.listdir(dataset_path))

    for folder in classes:

        folder_path = os.path.join(dataset_path, folder)

        if os.path.isdir(folder_path):
            st.write(f"{folder} : {len(os.listdir(folder_path))} images")

    fig = plt.figure(figsize=(15,8))

    for i, class_name in enumerate(classes):

        img_name = os.listdir(
            os.path.join(dataset_path, class_name)
        )[0]

        img_path = os.path.join(
            dataset_path,
            class_name,
            img_name
        )

        img = cv2.imread(img_path)

        if img is None:
            st.warning(f"Tidak dapat membaca gambar: {img_path}")
            continue

        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

        plt.subplot(2, 3, i + 1)
        plt.imshow(img)
        plt.title(class_name)
        plt.axis("off")

    plt.tight_layout()

    st.pyplot(fig)