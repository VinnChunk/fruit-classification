# ==========================================

def plot_confusion_matrix(

    y_test,

    prediction,

    classes,

    title

):

    fig, ax = plt.subplots(

        figsize=(8,6)

    )

    ConfusionMatrixDisplay.from_predictions(

        y_test,

        prediction,

        display_labels=classes,

        xticks_rotation=45,

        cmap="Blues",

        ax=ax

    )

    ax.set_title(title)

    return fig

